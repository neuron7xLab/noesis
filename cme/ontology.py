_DEFAULT = ContextCategory("Дія", "usa", "сенс = наслідки дії",
                           ("<implicit>",), "—", "активізм без рефлексії")


def extract_categories(raw: str) -> list[ActiveCategory]:
    """Повертає активні категорії (сигнал присутній у вході). Детерміновано."""
    low = raw.lower()
    active: list[ActiveCategory] = []
    for cat in ONTOLOGY:
        matched = [m for m in cat.signal if m in low]
        if matched:
            active.append(ActiveCategory(cat.name, cat.axis, cat.function, cat.failure_mode, matched))
    if not active:
        active.append(ActiveCategory(_DEFAULT.name, _DEFAULT.axis, _DEFAULT.function,
                                     _DEFAULT.failure_mode, ["<implicit:дія за замовчуванням>"]))
    return active


def build_reality_maps(active: list[ActiveCategory]) -> RealityMaps:
    """Групує активні категорії за осями у три карти; рахує домінанту й сплячі осі."""
    by_axis: dict[str, list[ActiveCategory]] = {a: [] for a in AXES}
    for cat in active:
        by_axis[cat.axis].append(cat)
    dominant = max(AXES, key=lambda a: len(by_axis[a]))
    dormant = [a for a in AXES if not by_axis[a]]
    return RealityMaps(
        europe=by_axis["europe"],
        usa=by_axis["usa"],
        china=by_axis["china"],
        dominant_axis=dominant,
        dormant_axes=dormant,
    )
